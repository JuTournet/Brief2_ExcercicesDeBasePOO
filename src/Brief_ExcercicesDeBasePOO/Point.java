package Brief_ExcercicesDeBasePOO;

public class Point {

	private double x = 0.0;
	private double y = 0.0;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	public Point(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}	
}
